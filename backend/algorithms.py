# SELECTOR UNIFICADO
# ============================================================
def run_custom_algorithm(name: str, grid, start, end):
    starts = normalize_points(start)
    ends = normalize_points(end)

    if name == "bfs":
        steps, path, origin = bfs_multi(grid, starts, ends)
    elif name == "dfs":
        steps, path, origin = dfs_multi(grid, starts, ends)
    elif name == "greedy":
        steps, path, origin = greedy_multi(grid, starts, ends)  # multi-origen
    elif name == "astar":
        steps, path, origin = astar_multi(grid, starts, ends)   # multi-origen
    else:
        raise ValueError("Unknown algorithm")

    return {
        "steps": steps,
        "path": path,
        "origin": origin
    }
