# A* MULTI-ORIGEN
# ============================================================
def astar_multi(grid, starts, ends):
    pq = []
    visited = {}
    came_from = {}
    gscore = {}
    steps = []

    for i, s in enumerate(starts):
        gscore[s] = 0
        heapq.heappush(pq, (heuristic_multi(s, ends), 0, i, s))
        visited[s] = i
        came_from[s] = None
        steps.append({"cell": [s[0], s[1]], "from_start": i, "initial": True})

    while pq:
        _, g, origin, (r, c) = heapq.heappop(pq)

        if came_from[(r, c)] is not None:
            steps.append({"cell": [r, c], "from_start": origin})

        if (r, c) in ends:
            return steps, reconstruct_path(came_from, starts[origin], (r, c)), origin

        for nr, nc in get_neighbors(grid, r, c):
            new_g = g + 1
            if (nr, nc) not in gscore or new_g < gscore[(nr, nc)]:
                gscore[(nr, nc)] = new_g
                visited[(nr, nc)] = origin
                came_from[(nr, nc)] = (r, c)
                f = new_g + heuristic_multi((nr, nc), ends)
                heapq.heappush(pq, (f, new_g, origin, (nr, nc)))

    return steps, [], -1

# ============================================================
# Validación de puntos
# ============================================================
def validate_points(grid: List[List[int]], points: List[Tuple[int, int]]) -> None:
    if not grid or not grid[0]:
        raise ValueError("Grid is empty")
    rows, cols = len(grid), len(grid[0])
    for r, c in points:
        if not (0 <= r < rows and 0 <= c < cols):
            raise IndexError(f"Point ({r}, {c}) is out of grid bounds (grid: {rows}x{cols})")
        if grid[r][c] != 0:
            raise ValueError(f"Point ({r}, {c}) is blocked (grid value: {grid[r][c]})")

# ============================================================
# SELECTOR UNIFICADO
# ============================================================
def run_custom_algorithm(name: str, grid, start, end):
    starts = normalize_points(start)
    ends = normalize_points(end)

    validate_points(grid, starts)
    validate_points(grid, ends)

    if name == "bfs":
        steps, path, origin = bfs_multi(grid, starts, ends)
    elif name == "dfs":
        steps, path, origin = dfs_multi(grid, starts, ends)
    elif name == "greedy":
        steps, path, origin = greedy_multi(grid, starts, ends)
    elif name == "astar":
        steps, path, origin = astar_multi(grid, starts, ends)
    else:
        raise ValueError("Unknown algorithm")

    return {
        "steps": steps,
        "path": path,
        "origin": origin
    }