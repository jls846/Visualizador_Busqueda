# BFS MULTI-ORIGEN
# ============================================================
def bfs_multi(grid, starts, ends):
    queue = deque()
    visited = {}
    came_from = {}
    steps = []

    # insertar todos los inicios
    for i, s in enumerate(starts):
        queue.append(s)
        visited[s] = i
        came_from[s] = None
        steps.append({"cell": [s[0], s[1]], "from_start": i, "initial": True})

    while queue:
        r, c = queue.popleft()
        origin = visited[(r, c)]

        # registrar exploración normal
        if came_from[(r, c)] is not None:
            steps.append({"cell": [r, c], "from_start": origin})

        # detener si llega a OTRO inicio
        if (r, c) in starts and came_from[(r, c)] is not None:
            return steps, reconstruct_path(came_from, starts[origin], (r, c)), origin

        # detener si llega a UN END
        if (r, c) in ends:
            return steps, reconstruct_path(came_from, starts[origin], (r, c)), origin

        for nr, nc in get_neighbors(grid, r, c):
            if (nr, nc) not in visited:
                visited[(nr, nc)] = origin
                came_from[(nr, nc)] = (r, c)
                queue.append((nr, nc))

    return steps, [], -1


