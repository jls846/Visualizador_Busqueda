# GREEDY MULTI-ORIGEN (arreglado)
# ============================================================
def greedy_multi(grid, starts, ends):
    pq = []
    visited = {}       # {(r,c): origin}
    came_from = {}
    steps = []

    # insertar todos los inicios
    for i, s in enumerate(starts):
        heapq.heappush(pq, (heuristic_multi(s, ends), i, s))  # (heurística, origin, celda)
        visited[s] = i
        came_from[s] = None
        steps.append({"cell": [s[0], s[1]], "from_start": i, "initial": True})

    while pq:
        _, origin, (r, c) = heapq.heappop(pq)

        if came_from[(r, c)] is not None:
            steps.append({"cell": [r, c], "from_start": origin})

        if (r, c) in ends:
            return steps, reconstruct_path(came_from, starts[origin], (r, c)), origin

        for nr, nc in get_neighbors(grid, r, c):
            if (nr, nc) not in visited:
                visited[(nr, nc)] = origin
                came_from[(nr, nc)] = (r, c)
                f = heuristic_multi((nr, nc), ends)
                heapq.heappush(pq, (f, origin, (nr, nc)))

    return steps, [], -1


