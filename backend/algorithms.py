# A* MULTI-ORIGEN (arreglado)
# ============================================================
def astar_multi(grid, starts, ends):
    pq = []
    visited = {}       # {(r,c): origin}
    came_from = {}
    gscore = {}
    steps = []

    for i, s in enumerate(starts):
        gscore[s] = 0
        heapq.heappush(pq, (heuristic_multi(s, ends), 0, i, s))  # (f, g, origin, celda)
        visited[s] = i
        came_from[s] = None
        steps.append({"cell": [s[0], s[1]], "from_start": i, "initial": True})

    while pq:
        _, g, origin, (r, c) = heapq.heappop(pq)

        if came_from[(r, c)] is not None:
            steps.append({"cell": [r, c], "from_start": origin})

        if (r, c) in ends:
            return steps, reconstruct_path(came_from, starts[origin], (r, c)), origin

        for nr, nc in get_neighbors(grid, r, c):
            new_g = g + 1
            if (nr, nc) not in gscore or new_g < gscore[(nr, nc)]:
                gscore[(nr, nc)] = new_g
                visited[(nr, nc)] = origin
                came_from[(nr, nc)] = (r, c)
                f = new_g + heuristic_multi((nr, nc), ends)
                heapq.heappush(pq, (f, new_g, origin, (nr, nc)))

    return steps, [], -1


