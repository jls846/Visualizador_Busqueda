# ============================================================
# Normalizador para que start acepte 1 o varios orígenes
# Formatos válidos:
#   start = [r,c]
#   start = [[r1,c1],[r2,c2],...]
# ============================================================
def normalize_points(data):
    if isinstance(data[0], int):          # UN SOLO INICIO
        return [(data[0], data[1])]
    return [(r, c) for r, c in data]      # VARIOS INICIOS


# ============================================================
# Vecinos válidos
# ============================================================
def get_neighbors(grid, r, c):
    moves = [(-1,0),(1,0),(0,-1),(0,1)]
    R, C = len(grid), len(grid[0])
    for dr, dc in moves:
        nr, nc = r + dr, c + dc
        if 0 <= nr < R and 0 <= nc < C and grid[nr][nc] == 0:
            yield (nr, nc)


# ============================================================
# Heurística Manhattan
# ============================================================
def heuristic(a, b):
    return abs(a[0] - b[0]) + abs(a[1] - b[1])


def heuristic_multi(p, ends):
    return min(heuristic(p, e) for e in ends)


# ============================================================
# Reconstrucción de ruta
# ============================================================
def reconstruct_path(came_from, start, end):
    path = []
    cur = end
    while cur is not None:
        path.append(cur)
        cur = came_from.get(cur)
    path.reverse()
    return path


