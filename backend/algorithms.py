# ============================================================
# Normalizador
# ============================================================
def normalize_points(data):
    if isinstance(data[0], int):
        return [(data[0], data[1])]
    return [(r, c) for r, c in data]

# ============================================================
# Vecinos válidos
# ============================================================
def get_neighbors(grid, r, c):
    moves = [(-1,0), (1,0), (0,1), (0,)]  # arriba, abajo, izq, der
    R, C = len(grid), len(grid[0])
    for dr, dc in moves:
        nr, nc = r + dr, c + dc
        if 0 <= nr < R and 0 <= nc < C and grid[nr][nc] == 0:
            yield (nr, nc)

# ============================================================
# Heurística
# ============================================================
def heuristic(a, b):
    return abs(a[0] - b[0]) + abs(a[1] - b[1])

def heuristic_multi(p, ends):
    return min(heuristic(p, e) for e in ends)

# ============================================================
# Reconstrucción de ruta
# ============================================================
def reconstruct_path(came_from, start, end):
    path = []
    cur = end
    while cur is not None:
        path.append(cur)
        cur = came_from.get(cur)
    path.reverse()
    return path

# ============================================================
# BFS MULTI-ORIGEN
# ============================================================
def bfs_multi(grid, starts, ends):
    queue = deque()
    visited = {}
    came_from = {}
    steps = []

    for i, s in enumerate(starts):
        queue.append(s)
        visited[s] = i
        came_from[s] = None
        steps.append({"cell": [s[0], s[1]], "from_start": i, "initial": True})

    while queue:
        r, c = queue.popleft()
        origin = visited[(r, c)]

        if came_from[(r, c)] is not None:
            steps.append({"cell": [r, c], "from_start": origin})

        if (r, c) in ends:
            return steps, reconstruct_path(came_from, starts[origin], (r, c)), origin

        for nr, nc in get_neighbors(grid, r, c):
            if (nr, nc) not in visited:
                visited[(nr, nc)] = origin
                came_from[(nr, nc)] = (r, c)
                queue.append((nr, nc))

    return steps, [], -1

# ============================================================
# DFS MULTI-ORIGEN 
# ============================================================
def dfs_multi(grid, starts, ends):
    stack = []
    visited = {}
    came_from = {}
    steps = []

    # Inicializar todos los orígenes
    for i, s in enumerate(starts):
        stack.append((s, i))
        visited[s] = i
        came_from[s] = None
        steps.append({
            "cell": [s[0], s[1]],
            "from_start": i,
            "initial": True
        })

    while stack:
        (r, c), origin = stack.pop()

        # Registrar el paso (si no es inicio)
        if came_from[(r, c)] is not None:
            steps.append({"cell": [r, c], "from_start": origin})

        if (r, c) in ends:
            return (
                steps,
                reconstruct_path(came_from, starts[origin], (r, c)),
                origin
            )
        neighbors = list(get_neighbors(grid, r, c))
        
        for nr, nc in reversed(neighbors):
            if (nr, nc) not in visited:
                visited[(nr, nc)] = origin
                came_from[(nr, nc)] = (r, c)
                stack.append(((nr, nc), origin))

    return steps, [], -1
